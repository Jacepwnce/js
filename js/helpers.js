/* helpers.js */

/** @param {string} name */
export const getNumberOfChars = name => {
    //return the number of characters in: name
      // your code
      return name.length
  };
  
  
  /** @param {string} name */
  export const getLower = name => {
    //return name all in lower case (example: "ABC" becomes "abc")
      // your code
      return name.toLocaleLowerCase()
  };
  
  /** @param {string} name */
  export const getUpper = name => {
    //return name all in upper case (example: "abc" becomes "ABC")
      // your code
      return name.toLocaleUpperCase()
  };